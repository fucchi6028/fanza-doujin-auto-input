def get_keyword_name(keyword_id: str) -> str:
    """キーワードIDから名前を取得"""
    # 人気キーワードから検索
    for kid, name in POPULAR_KEYWORDS:
        if kid == keyword_id:
            return name

    # カテゴリ別キーワードから検索
    for category, keywords in KEYWORDS_BY_CATEGORY.items():
        for kid, name, _ in keywords:
            if kid == keyword_id:
                return name

    return keyword_id
